package Bean;

public class SachBean {
	private String masach;
	private String tensach;
	private int soluong;
	private int giaban;
	private String maloai;
	private String sotap;
	private String anhbia;
	private String ngaynhap;
	private String tacgia;
	

	public SachBean(String masach, String tensach, int soluong, int giaban, String maloai, String sotap, String anhbia,
			String ngaynhap, String tacgia) {
		super();
		this.masach = masach;
		this.tensach = tensach;
		this.soluong = soluong;
		this.giaban = giaban;
		this.maloai = maloai;
		this.sotap = sotap;
		this.anhbia = anhbia;
		this.ngaynhap = ngaynhap;
		this.tacgia = tacgia;
	}



	public String getMasach() {
		return masach;
	}



	public void setMasach(String masach) {
		this.masach = masach;
	}



	public String getTensach() {
		return tensach;
	}



	public void setTensach(String tensach) {
		this.tensach = tensach;
	}



	public int getSoluong() {
		return soluong;
	}



	public void setSoluong(int soluong) {
		this.soluong = soluong;
	}



	public int getGiaban() {
		return giaban;
	}



	public void setGiaban(int giaban) {
		this.giaban = giaban;
	}



	public String getMaloai() {
		return maloai;
	}



	public void setMaloai(String maloai) {
		this.maloai = maloai;
	}



	public String getSotap() {
		return sotap;
	}



	public void setSotap(String sotap) {
		this.sotap = sotap;
	}



	public String getAnhbia() {
		return anhbia;
	}



	public void setAnhbia(String anhbia) {
		this.anhbia = anhbia;
	}



	public String getNgaynhap() {
		return ngaynhap;
	}



	public void setNgaynhap(String ngaynhap) {
		this.ngaynhap = ngaynhap;
	}



	public String getTacgia() {
		return tacgia;
	}



	public void setTacgia(String tacgia) {
		this.tacgia = tacgia;
	}



	public SachBean() {
		super();
	}
	
	
}
